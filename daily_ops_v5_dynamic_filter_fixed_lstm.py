                    # æ§‹å»º Sell Agent çš„è§€å¯Ÿå€¼ (ç‰¹å¾µ + å ±é…¬ç‡)
                    s_feat = np.concatenate([features[0], [current_return]]).reshape(1, -1).astype(np.float32)
