    features = np.nan_to_num(features, nan=0.0, posinf=1.0, neginf=-1.0)
    
    # è¼‰å…¥ V5 æ¨¡å‹
    buy_path = os.path.join(V5_MODEL_PATH, 'ppo_buy_twii_final.zip')
