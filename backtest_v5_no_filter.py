class V5Backtester:
    """
    V5 å›æ¸¬å™¨ï¼šç„¡æ¿¾ç¶²é™åˆ¶
    """
    def __init__(self, buy_model, sell_model, initial_capital=1_000_000, 
                 sell_threshold=0.6, buy_consensus_threshold=0.5):
        self.buy_model = buy_model
        self.sell_model = sell_model
        self.initial_capital = initial_capital
        self.sell_threshold = sell_threshold
        self.buy_consensus_threshold = buy_consensus_threshold
        self.trades = []
        self.equity_curve = []
        self.buy_signals = []
        self.sell_signals = []
        self.daily_confidence = []
    
    def run(self, df: pd.DataFrame, feature_cols: list) -> dict:
        """åŸ·è¡Œå›æ¸¬"""
        capital = self.initial_capital
        position = None
        
        features = df[feature_cols].values.astype(np.float32)
        close_prices = df['Close'].values
        dates = df.index
            
        print(f"[Backtest] å›æ¸¬æœŸé–“: {dates[0].strftime('%Y-%m-%d')} ~ {dates[-1].strftime('%Y-%m-%d')}")
        print(f"[Backtest] è³‡æ–™ç­†æ•¸: {len(df)}")
        print(f"[Backtest] åˆå§‹è³‡é‡‘: ${capital:,.0f}")
        print("[Backtest] æ¨¡å¼: V5 ç„¡æ¿¾ç¶² (å°ç¨±çå‹µ)")
        
        for i in range(len(df)):
            date = dates[i]
            price = close_prices[i]
            
            # è¨˜éŒ„æ¯æ—¥æ·¨å€¼
            if position is not None:
                current_value = capital + position['shares'] * price
            else:
                current_value = capital
            self.equity_curve.append({'date': date, 'value': current_value})
            
            # è™•ç† NaN
            obs = np.nan_to_num(features[i], nan=0.0, posinf=1.0, neginf=-1.0)
            
            # =================================================================
            # æŒæœ‰ä¸­ï¼šæª¢æŸ¥è³£å‡º
            # =================================================================
            if position is not None:
                hold_days = i - position['buy_idx']
                current_return = price / position['buy_price']
                
                sell_obs = np.concatenate([obs, [current_return]]).astype(np.float32).reshape(1, -1)
                action, _ = self.sell_model.predict(sell_obs, deterministic=True)
                
                sell_obs_tensor = self.sell_model.policy.obs_to_tensor(sell_obs)[0]
                sell_probs = self.sell_model.policy.get_distribution(sell_obs_tensor).distribution.probs.detach().cpu().numpy()[0]
                sell_confidence = float(sell_probs[1]) if action[0] == 1 else float(sell_probs[0])
                
                # ğŸ”¥ Agent Consensus Check: å–å¾— Buy Agent å°ç•¶å‰å¸‚å ´çš„ä¿¡å¿ƒ
                # (å³ä½¿ç›®å‰æ˜¯æŒæœ‰ç‹€æ…‹ï¼Œä¹Ÿè¦å• Buy Agent æ€éº¼çœ‹)
                buy_obs_check = obs.reshape(1, -1)
                buy_action_check, _ = self.buy_model.predict(buy_obs_check, deterministic=True)
                buy_check_tensor = self.buy_model.policy.obs_to_tensor(buy_obs_check)[0]
                buy_check_probs = self.buy_model.policy.get_distribution(buy_check_tensor).distribution.probs.detach().cpu().numpy()[0]
                # å–å¾— Buy Confidence (çœ‹æ¼²æ©Ÿç‡)
                current_buy_conf = float(buy_check_probs[1]) if buy_action_check[0] == 1 else float(buy_check_probs[0])
                if buy_action_check[0] == 0: # å¦‚æœæ˜¯çœ‹è·Œ/è§€æœ›ï¼Œä¿¡å¿ƒè¦è½‰æ›å—ï¼Ÿä¸ï¼Œæˆ‘å€‘éœ€è¦çš„æ˜¯ã€Œæƒ³è²·ã€çš„ä¿¡å¿ƒ
                     # StableBaselines3 çš„ probs æ˜¯ [prob_0, prob_1]
                     # å¦‚æœ action æ˜¯ 0 (WAIT), conf æ˜¯ prob_0. æˆ‘å€‘éœ€è¦ prob_1 (BUYæ©Ÿç‡) ä¾†åˆ¤æ–·å¤šé ­å…±è­˜
                     current_buy_conf = float(buy_check_probs[1]) 

                self.daily_confidence.append({
                    'date': date, 'status': 'holding', 'price': price,
                    'buy_conf': current_buy_conf, 'sell_conf': sell_confidence,
                    'sell_action': 'SELL' if action[0] == 1 else 'HOLD',
                    'current_return': current_return
                })
                
                stop_loss = current_return < 0.92
                is_sell_signal = (action[0] == 1 and sell_confidence > self.sell_threshold)
                
                # Consensus Veto
                is_vetoed = False
                if is_sell_signal and not stop_loss:
                    if current_buy_conf > self.buy_consensus_threshold:
                        is_vetoed = True
                        # print(f"  ğŸ›‘ [{date.strftime('%Y-%m-%d')}] Sell Vetoed! SellConf:{sell_confidence:.2f} BuyProb:{current_buy_conf:.2f}")

                should_sell = (is_sell_signal and not is_vetoed) or stop_loss
                
                if should_sell:
                    sell_value = position['shares'] * price
                    profit = sell_value - position['shares'] * position['buy_price']
                    capital += sell_value
                    
                    sell_reason = 'stop_loss' if stop_loss else 'ai_signal'
                    
                    self.trades.append({
                        'buy_date': position['buy_date'],
                        'buy_price': position['buy_price'],
                        'buy_confidence': position.get('buy_confidence', 0),
                        'sell_date': date,
                        'sell_price': price,
                        'sell_confidence': sell_confidence,
                        'sell_reason': sell_reason,
                        'return': current_return - 1,
                        'profit': profit,
                        'hold_days': hold_days
                    })
                    self.sell_signals.append((date, price, sell_confidence))
                    position = None
            
            # =================================================================
            # ç©ºæ‰‹ï¼šæª¢æŸ¥è²·å…¥ (ç„¡æ¿¾ç¶²)
            # =================================================================
            elif position is None:
                buy_obs = obs.reshape(1, -1)
                action, _ = self.buy_model.predict(buy_obs, deterministic=True)
                
                buy_obs_tensor = self.buy_model.policy.obs_to_tensor(buy_obs)[0]
                buy_probs = self.buy_model.policy.get_distribution(buy_obs_tensor).distribution.probs.detach().cpu().numpy()[0]
                buy_confidence = float(buy_probs[1]) if action[0] == 1 else float(buy_probs[0])
                
                ai_buy = action[0] == 1
                
                self.daily_confidence.append({
                    'date': date, 'status': 'idle', 'price': price,
                    'buy_conf': buy_confidence, 'sell_conf': None,
                    'buy_action': 'BUY' if ai_buy else 'WAIT',
                    'current_return': None
                })
                
                if ai_buy:
                    invest_amount = capital * 0.9
                    shares = int(invest_amount / price)
                    
                    if shares > 0:
                        cost = shares * price
                        capital -= cost
                        
                        position = {
                            'shares': shares,
                            'buy_price': price,
                            'buy_date': date,
                            'buy_idx': i,
                            'buy_confidence': buy_confidence
                        }
                        self.buy_signals.append((date, price, buy_confidence))
        
        return self._calculate_metrics(df)
    
    def _calculate_metrics(self, df: pd.DataFrame) -> dict:
        """è¨ˆç®—ç¸¾æ•ˆæŒ‡æ¨™"""
        if not self.equity_curve:
            return {}
        
        equity_df = pd.DataFrame(self.equity_curve)
        equity_df['date'] = pd.to_datetime(equity_df['date'])
        equity_df.set_index('date', inplace=True)
        
        initial = self.initial_capital
        final = equity_df['value'].iloc[-1]
        total_return = (final - initial) / initial
        
        days = (equity_df.index[-1] - equity_df.index[0]).days
        years = days / 365.0
        annualized_return = (1 + total_return) ** (1 / years) - 1 if years > 0 else 0
        
        daily_returns = equity_df['value'].pct_change().dropna()
        if len(daily_returns) > 0 and daily_returns.std() > 0:
            sharpe = (daily_returns.mean() * 252 - 0.02) / (daily_returns.std() * np.sqrt(252))
        else:
            sharpe = 0
        
        rolling_max = equity_df['value'].cummax()
        drawdown = (equity_df['value'] - rolling_max) / rolling_max
        max_drawdown = drawdown.min()
        
        if self.trades:
            wins = sum(1 for t in self.trades if t['return'] > 0)
            win_rate = wins / len(self.trades)
            avg_return = np.mean([t['return'] for t in self.trades])
            avg_hold_days = np.mean([t['hold_days'] for t in self.trades])
        else:
            win_rate = 0
            avg_return = 0
            avg_hold_days = 0
        
        return {
            'initial_capital': initial,
            'final_value': final,
            'total_return': total_return,
            'annualized_return': annualized_return,
            'sharpe_ratio': sharpe,
            'max_drawdown': max_drawdown,
            'total_trades': len(self.trades),
            'win_rate': win_rate,
            'avg_return': avg_return,
            'avg_hold_days': avg_hold_days,
            'equity_df': equity_df
        }


